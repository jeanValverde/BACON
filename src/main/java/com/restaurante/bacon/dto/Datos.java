package com.restaurante.bacon.dto;

/**
 *
 * @author jean
 */
public class Datos {
    private String tipo;
    private String llave;
    private String clave;

    public Datos() {
    }

    public Datos(String tipo, String llave, String clave) {
        this.tipo = tipo;
        this.llave = llave;
        this.clave = clave;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getLlave() {
        return llave;
    }

    public void setLlave(String llave) {
        this.llave = llave;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }
    
    
    
}
